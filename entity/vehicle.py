
class Vehicle:
    def __init__(
        self,
        VehicleID,
        Model,
        Make,
        Year,
        Color,
        RegistrationNumber,
        Availability,
        DailyRate,
    ):
        self.VehicleID = VehicleID
        self.Model = Model
        self.Make = Make
        self.Year = Year
        self.Color = Color
        self.RegistrationNumber = RegistrationNumber
        self.Availability = Availability
        self.DailyRate = DailyRate

